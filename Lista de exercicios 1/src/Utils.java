public class Utils{
    static double calculaMedia(double x,double y){
        return (x+y)/2;
    }
    static double calculaMedia(double x, double y, double z){
        return (x+y+z)/3;
    }
    static double calculaMedia(double a, double b, double c, double d){
        return (a+b+c+d)/4;
    }
}